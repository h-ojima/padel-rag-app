def seed():
    api_key = get_openai_api_key()
    if not api_key:
        print("[ERROR] OPENAI_API_KEY が設定されていません。.env を確認してください。")
        sys.exit(1)

    # Windows環境のSSL問題対策：httpxにSSL検証無効のクライアントを渡す
    import httpx
    http_client = httpx.Client(verify=False)
    embeddings = OpenAIEmbeddings(model=EMBEDDING_MODEL, api_key=api_key, http_client=http_client)
    splitter = RecursiveCharacterTextSplitter(
        chunk_size=CHUNK_SIZE,
        chunk_overlap=CHUNK_OVERLAP,
        separators=["\n\n", "\n", "。", "、", " ", ""],
    )

    print(f"[START] {len(SEED_MEMOS)} 件のメモを投入します...\n")

    for i, memo in enumerate(SEED_MEMOS, 1):
        category = memo["category"]
        text = memo["text"]

        memo_id = str(uuid.uuid4())
        timestamp = datetime.now().isoformat()

        # ChromaDBに保存
        vectorstore = Chroma(
            collection_name=category,
            embedding_function=embeddings,
            persist_directory=str(CHROMA_DIR),
        )
        chunks = splitter.split_text(text)
        metadatas = [
            {"memo_id": memo_id, "category": category, "created_at": timestamp, "chunk_index": j}
            for j in range(len(chunks))
        ]
        vectorstore.add_texts(texts=chunks, metadatas=metadatas)

        # JSONにも二重保存（再起動時の再インデックス用）
        record = {
            "memo_id": memo_id,
            "category": category,
            "text": text,
            "created_at": timestamp,
        }
        memo_file = MEMOS_DIR / f"{memo_id}.json"
        memo_file.write_text(json.dumps(record, ensure_ascii=False, indent=2), encoding="utf-8")

        print(f"  [{i:02d}/{len(SEED_MEMOS)}] [{category}] {text[:40].strip()}...")

    print(f"\n[DONE] {len(SEED_MEMOS)} 件を ChromaDB と JSON に保存しました。")


if __name__ == "__main__":
    seed()
